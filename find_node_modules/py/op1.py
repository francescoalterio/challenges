import os

class Node_Modules:
    def __init__(self, init_path):
        self.init_path = init_path

    def get_node_modules(self):
        current_path = ""
        dirs_to_find = [self.init_path]
        node_modules = []

        while len(dirs_to_find) > 0:
            current_path = dirs_to_find.pop()
            dir_content = os.listdir(current_path)
            for file in dir_content:
                file_path = os.path.join(current_path, file)
                if os.path.isdir(file_path):
                    if file == "node_modules":
                        node_modules.append(file_path)
                    else:
                        dirs_to_find.append(file_path)

        return node_modules
    
# test
node_modules = Node_Modules(os.getcwd())
all_node_modules = node_modules.get_node_modules()
for node_module in all_node_modules:
    print(node_module)